# Pillars of OOP

# Abstraction - making complicated action easier for our users e.g. accelerate, and it happens

# Encapsulation - hiding away the dangerous stuff to make things safe - restricting direct access
# - abstract it away..

# Inheritance - inherit existing features and change things around
# e.g. humanoid class in games, enemies need to be same but do something different, all can
# inherit from a character but can be customised

# Polymorphism - Things don't have to work the same way in parents and child classes
# e.g. lever go down in 2 slot toaster, but in 4 slot toaster the lever can be adjusted to fit needs
